#### 지도학습(Supervised Learning) : 미리 정의된 범주(클래스) 중 하나로 분류하는 모델을 학습
# 입력 데이터 (Features)	모델이 학습할 수 있는 정보. 예: 이메일의 단어들
# 레이블 (Label)	각 데이터가 속한 정답 클래스. 예: 스팸 or 정상
# 모델 (Classifier)	입력을 받아 어떤 클래스에 속하는지 예측하는 함수
# 학습 (Training)	입력과 레이블을 이용해 모델의 파라미터를 조정하는 과정
# 예측 (Prediction)	학습된 모델이 새로운 입력에 대해 클래스를 추정하는 것

### 대표 알고리즘
# 로지스틱 회귀 (Logistic Regression) : 확률 기반의 간단한 분류
# 서포트 벡터 머신 (SVM) : 결정 경계를 최대화
# 의사결정나무 (Decision Tree) : 조건에 따라 분기하며 분류
# 랜덤 포레스트 (Random Forest) : 여러 나무를 조합해 예측
# k-최근접 이웃 (k-NN) : 주변 데이터와 유사한 클래스로 분류
# 신경망 (Neural Networks) : 복잡한 패턴을 학습 가능, 딥러닝 기반

### 성능 평가
# 정확도 (Accuracy): 전체 중 맞춘 비율
# 정밀도 (Precision): 예측한 것 중 실제로 맞은 비율
# 재현율 (Recall): 실제 정답 중 얼마나 잘 맞췄는지
# F1 점수: 정밀도와 재현율의 조화 평균